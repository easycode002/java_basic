package Day01;

public class OperatorDemo03 {
	public static void main(String[] args) {
		int a = 10;
		int b = 5;
		// Sum
		int sum = a + b;
		// Subtraction
		int sub = a - b;
		// Multiplication
		int multi = a * b;
		// Division
		int div = a / b;
		System.out.println(sum + ", " + sub + ", " + multi + ", " + div);
	}
}

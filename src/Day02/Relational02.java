package Day02;
// Relational operators compare two values and return a boolean (true or false).
public class Relational02 {
	public static void main(String[] args) {
		int x, y;
		x = 10;
		y = 30;
		System.out.println("x == y : " + (x == y));
		System.out.println("x != y : " + (x != y));
		System.out.println("x > y  : " + (x > y));
		System.out.println("x < y  : " + (x < y));
		System.out.println("x <= y : " + (x <= y));
		System.out.println("x >= y : " + (x >= y));
	}
}

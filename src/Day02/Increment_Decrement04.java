package Day02;

public class Increment_Decrement04 {
	public static void main(String[] args) {
		int num1, num2;
		num1 = 20;
		System.out.println("++num1 : "+(++num1));
		System.out.println("num1++ : "+(num1++));
		num2 = 12;
		System.out.println("--num2 : "+(--num2));
		System.out.println("num2-- : "+(num2--));
	}
}
